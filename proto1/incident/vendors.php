                    Interested in doing business with the Forest Service?
                </h3>
                <p>
                    Here are the basics of what is needed to become compliant with Government regulations and interact with our systems.
                </p>
                <ol id="biglist">
                    <li>
                        <div class="listnumber">1</div>
                        <p><span class="heading">
                                Obtain an eMail Address: 
                            </span>
                            If you don’t already have one, obtain a valid email address.  You can obtain one from your internet service provider (ISP) or you can use one of the many <a href="http://email.about.com/od/freeemailreviews/tp/free_email.htm">free services</a> available on the Web. A valid email address is required to register with Data Universal Number System (DUNS), System for Award Management (SAM), and eAuthentication.
                        </p>
                    </li>
                    <li>
                        <div class="listnumber">2</div>
                        <p><span class="heading">
                                Obtain a D-U-N-S® Number: 
                            </span>
                            The Data Universal Number System code is required by the Federal Acquisition Regulations <a href="http://acquisition.gov/far/current/html/52_200_206.html#wp1137830">(FAR 52.204-6)</a> and you must have this before registering in the System for Award Management (SAM). Register online at <a href="http://fedgov.dnb.com/webform/">D&amp;B DUNS Number site for government contractors</a>.</p>
                    </li>
                    <li>
                        <div class="listnumber">3</div>
                        <p><span class="heading">
                                Obtain an IRS TIN: 
                            </span>
                            For invoicing, payment, and tax purposes, you must have a <a href="http://www.irs.gov/businesses/small/international/section/0,,id=96696,00.html">Taxpayer Identification Number</a> as required by the IRS.  For privacy and security purposes, we recommend that you do not use your Social Security Number (SSN).</p>
                    </li>
                    <li>
                        <div class="listnumber">4</div>
                        <p><span class="heading">
                                Register with SAM: 
                            </span>
                            In order to do business with the Federal Government (as required by <a href="http://acquisition.gov/far/current/html/Subpart%204_11.html#wp1073569">FAR 4.11</a>), you must register in the <a href="https://www.sam.gov">System for Award Management (SAM)</a>.  You are required to validate your information in SAM once per year. Even if you do not have any updates, you must access SAM at least once every 12 months to keep your SAM account active. The Government cannot make payments to vendors with an expired or inactive SAM account.</p>
                    </li>
                    <li>
                        <div class="listnumber">5</div>
                        <p><span class="heading">Register for a USDA eAuthentication account: </span>
                            The individual from your company who will be signing your preseason incident agreement with the Forest Service must obtain a <a href="eauth.php">Level 2 eAuthentication account</a> in order to transact business with the government. NOTE: You MUST access your eAuthentication account at least once every 90 days to keep your account from being deactiviated.
                        </p>
                    </li>
                    <li>
                        <div class="listnumber">6</div>
                        <p><span class="heading">Register at FedBizOpps and watch for business opportunities: </span>
                            Every competitive solicitation we offer is first posted to <a href="https://www.fbo.gov/index">FBO</a>, which provides you with government-wide business opportunities.  Every solicitation is different, so carefully read the instructions on how to submit your offers.</p>
                    </li>

                </ol>
