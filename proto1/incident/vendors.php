                <h3>Announcements</h3>
                <dl>
                    <dt>VIPR Is Interconnected with the System for Award Management (SAM)</dt>
                    <dd>
                        <p>In order to do business with the Government, vendors MUST have an active SAM account. For vendors who use VIPR, they MUST ensure certain company information they provide in VIPR matches the same type of information that is found in their SAM record. VIPR and SAM share the following vendor company information:</p>
                        <ul>
                            <li>DUNS</li>
                            <li>Company Name</li>
                            <li>Company DBA</li>
                            <li>Company Address</li>
                            <li>Mailing Address</li>
                        </ul>
                        <p>
                            Ensuring the above information matches in both systems is critical in order for payments to be made to vendors.
                        </p>
                        <p>
                            To assist vendors with keeping their company information in sync across both systems, VIPR now has an interconnection with SAM. VIPR will do validation checks against SAM data throughout various phases in the procurement process:
                        </p>
                        <ul>
                            <li>When a vendor registers in the VIPR vendor application, VIPR will display the company name and DBA from SAM so the vendor can verify they entered the DUNS correctly. </li>
                            <li>A vendor will be able to submit quotes to VIPR solicitations; however, if their SAM record is expired or their company information in VIPR does not match what is in SAM, then the vendor will be ineligible to receive awards by Contracting Officers.</li>
                            <li>If a vendor has changed their company information in SAM and they have not updated that company information in VIPR to reflect those changes, then the vendor will be unable to respond to modifications to their agreement until they import their updated company information from SAM into VIPR. (The vendor application provides vendors with an "Import from SAM" function.)</li>
                            <li>If a vendor's SAM record has expired since receiving their award, then the vendor will be unable to respond to modifications to their agreement until they get their SAM record re-activated. </li>
                        </ul>
                    </dd>
